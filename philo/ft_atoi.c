#include "philo.h"

int	ft_atoi(char *str)
{
	long	i;
	int		s;
	long	o;

	i = 0;
	o = 0;
	s = 1;
	
	while ((str[i] != '\0') && (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13)))
		i++;
	if (str[i] == '-' && (str[i + 1] >= '0' && str[i + 1] <= '9'))
	{
		i++;
		s = -1;
	}
	if (str[i] == '+' && (str[i + 1] >= '0' && str[i + 1] <= '9'))
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		o *= 10;
		o += str[i] - 48;
		i++;
	}
	o *= s;
	return (o);
}
